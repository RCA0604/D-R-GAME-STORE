DROP TABLE Categoria
GO
CREATE TABLE Categoria
(
	Id INT IDENTITY PRIMARY KEY,
	Descripcion VARCHAR(50) NOT NULL,
	EstadoId INT NOT NULL,
	FechaCreacion DATETIME NOT NULL,
	UsuarioCreacion VARCHAR(50) NOT NULL
)
GO

DROP TABLE Plataforma
GO
CREATE TABLE Plataforma 
(
	Id INT IDENTITY PRIMARY KEY,
	Descripcion VARCHAR(50) NOT NULL,
	EstadoId INT NOT NULL,
	FechaCreacion DATETIME NOT NULL,
	UsuarioCreacion VARCHAR(50) NOT NULL
)
GO

DROP TABLE Estado
GO
CREATE TABLE Estado
(
	Id INT IDENTITY PRIMARY KEY,
	Descripcion VARCHAR(50) NOT NULL,
	FechaCreacion DATETIME NOT NULL,
	UsuarioCreacion VARCHAR(50) NOT NULL
)
GO

DROP TABLE Producto
GO
CREATE TABLE Producto
(
	Id INT IDENTITY PRIMARY KEY,
	Descripcion VARCHAR(80) NOT NULL,
	Codigo VARCHAR(80) NOT NULL,
	CodigoVenta UNIQUEIDENTIFIER NOT NULL,
	CategoriaId INT  REFERENCES Categoria(Id) NOT NULL,
	Stock INT NOT NULL,
	PrecioBase DECIMAL(18,5) NOT NULL,
	Descuento DECIMAL(18,5) NULL,
	PrecioFinal DECIMAL(18,5) NULL,
	PlataformaId INT REFERENCES Plataforma(Id) NOT NULL,
	EstadoId INT REFERENCES Estado(Id) NOT NULL,
	UsuarioCreacion VARCHAR(80) NOT NULL,
	FechaCreacion DATETIME NOT NULL, 
	UsuarioModificacion VARCHAR(80) NULL,
	FechaModificacion DATETIME NULL, 
	UsuarioCompra VARCHAR(80) NULL,
	FechaCompra VARCHAR(80) NULL   
)
GO





